function makeTags(
  district: string,
  party: string
): { label: string; weight: number }[] {
  const ds = baseTags[district] ?? ["地域課題"];
  const ps = partyTags[party] ?? [];
  const picks = [...ds.slice(0, 3), ...ps.slice(0, 2)];
  return picks.map((label, i) => ({ label, weight: 10 - i * 2 }));
}

