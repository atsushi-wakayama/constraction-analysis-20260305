// 年齢バイアス
function ageBias(age: number): Partial<Record<string, number>> {
  if (age < 40) return { 教育: 10, "環境・脱炭素": 10, "産業・観光": 5 };
  if (age >= 70) return { "インフラ・交通": 8, "防災・減災": 8, 農林水産: 5 };
  return {};
}

function clamp(v: number, min = 20, max = 100) {
  return Math.min(max, Math.max(min, v));
}

function makeTopics(
  district: string,
  party: string,
  age: number
): { axis: string; value: number }[] {
  const base = districtBias(district);
  const pb = partyBias(party);
  const ab = ageBias(age);
  return defaultTopicAxes.map((axis, i) => {
    const value = clamp(base[i] + (pb[axis] ?? 0) + (ab[axis] ?? 0));
    return { axis, value };
  });
}

